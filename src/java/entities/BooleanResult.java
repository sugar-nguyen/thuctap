package entities;

/**
 *
 * @author sugar
 */
public class BooleanResult {
    private boolean status;

    public BooleanResult() {
    }

    public BooleanResult(boolean status) {
        this.status = status;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
    
}
